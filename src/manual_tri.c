#include "push_swap.h"

void	sort_three(int *a)
{
	if (a[0] > a[1] && a[0] > a[2])
	{
		if (a[1] > a[2])
		{
			sa(a, 3);
			rra(a, 3);
		}
		else
			ra(a, 3);
	}
	else if (a[0] > a[1] && a[0] < a[2])
		sa(a, 3);
	else if (a[0] < a[1] && a[0] > a[2])
		rra(a, 3);
	else if (a[0] < a[1] && a[0] < a[2])
	{
		if (a[1] > a[2])
		{
			rra(a, 3);
			sa(a, 3);
		}
	}
}

void	sort_two(int *a)
{
	sa(a, 2);
}
